public class Mathe_operators {
    public static void main(String[] args) {
        int a = 12;
        int b = 78;
        int res_add = a + b;
        int res_sub = a - b;
        int res_mul = a * b;
        double res_div = a / (b*1.0);
        int res_mod = a % b;
        System.out.println("res_add = " + res_add);;
        System.out.println("res_sub = " + res_sub);;
        System.out.println("res_mul = " + res_mul);;
        System.out.println("res_div = " + res_div);;
        System.out.println("res_mod = " + res_mod);;
    }
}
